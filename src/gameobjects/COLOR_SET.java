package gameobjects;

/**
 * Enum class representing all possible color sets in a standard Monopoly board.
 * Special squares - e.g. Chance, Free Parking - are grouped under the FUNCTION enum.
 */
public enum COLOR_SET {

    BROWN,
    LIGHT_BLUE,
    PURPLE,
    ORANGE,
    RED,
    YELLOW,
    GREEN,
    DARK_BLUE,
    RAILROAD,
    UTILITY,
    FUNCTION

}